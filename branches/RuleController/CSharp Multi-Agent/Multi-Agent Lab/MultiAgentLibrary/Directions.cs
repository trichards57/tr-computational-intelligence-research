namespace MultiAgentLibrary
{
    /// <summary>
    /// Used to represent a direction the pod can move in.
    /// </summary>
    public enum Direction
    {
        /// <summary>
        /// Moves to a smaller y coordinate.
        /// </summary>
        Up,
        /// <summary>
        /// Moves to a larger y coordinate.
        /// </summary>
        Down,
        /// <summary>
        /// Moves to a smaller x coordinate.
        /// </summary>
        Left,
        /// <summary>
        /// Moves to a larger x coordinate.
        /// </summary>
        Right
    }
}